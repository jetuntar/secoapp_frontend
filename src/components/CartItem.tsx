interface CartItemProps {
  quantity:string,
  id: string;
  name: string;
  imagelink_square: string;
  special_ingredient: string;
  roasted: string;
  price: any;
  type: string;
  incrementCartItemQuantityHandler: any;
  decrementCartItemQuantityHandler: any;
}

const CartItem: React.FC<CartItemProps> = ({
  id,
  quantity,
  name,
  imagelink_square,
  special_ingredient,
  roasted,
  price,
  type,
  incrementCartItemQuantityHandler,
  decrementCartItemQuantityHandler,
}) => {
  const [coffeeItem, setCoffeeItem] = useState<any>(null);

  useEffect(() => {
    const fetchCoffeeItem = async () => {
      try {
        // console.log(id);
        const response = await fetch(`${apiUrl}/api/coffee-item/${id}`); // Use id from props
        if (!response.ok) {
          throw new Error('Failed to fetch coffee item');
        }
        const data = await response.json();
        setCoffeeItem(data);
      } catch (error) {
        console.error(error);
        // Handle error here
      }
    };

    fetchCoffeeItem();
  }, [id]);

  return (
    <View>
      {coffeeItem && (
        <>
        <LinearGradient
          start={{x: 0, y: 0}}
          end={{x: 1, y: 1}}
          colors={[COLORS.primaryGreyHex, COLORS.primaryBlackHex]}
          style={styles.CartItemLinearGradient}>
          <View style={styles.CartItemRow}>
            <Image source={{uri: coffeeItem.imagelink_square}} style={styles.CartItemImage} />
            <View style={styles.CartItemInfo}>
              <View>
                <Text style={styles.CartItemTitle}>{coffeeItem.name}</Text>
                <Text style={styles.CartItemSubtitle}>
                  {coffeeItem.special_ingredient}
                </Text>
              </View>
            </View>
          </View>
            <View
              style={styles.CartItemSizeRowContainer}>
              <View style={styles.CartItemSizeValueContainer}>
                <Text style={styles.SizeCurrency}>
                  <Text style={styles.SizePrice}>$ {coffeeItem.price}</Text>
                </Text>
              </View>
              <View style={styles.CartItemSizeValueContainer}>
                <TouchableOpacity
                  style={styles.CartItemIcon}
                  onPress={() => {
                    decrementCartItemQuantityHandler(id);
                  }}>
                  <CustomIcon
                    name="minus"
                    color={COLORS.primaryWhiteHex}
                    size={FONTSIZE.size_10}
                  />
                </TouchableOpacity>
                <View style={styles.CartItemQuantityContainer}>
                  <Text style={styles.CartItemQuantityText}>
                    {quantity}
                  </Text>
                </View>
                <TouchableOpacity
                  style={styles.CartItemIcon}
                  onPress={() => {
                    incrementCartItemQuantityHandler(id);
                  }}>
                  <CustomIcon
                    name="add"
                    color={COLORS.primaryWhiteHex}
                    size={FONTSIZE.size_10}
                  />
                </TouchableOpacity>
              </View>
            </View>
        </LinearGradient>
        </>
      )}
    </View>
  );
};

